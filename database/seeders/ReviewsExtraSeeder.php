<?php

namespace Database\Seeders;

use App\Models\Product;
use App\Models\Review;
use App\Models\User;
use Illuminate\Database\Seeder;

class ReviewsExtraSeeder extends Seeder
{
    public function run(): void
    {
        $userIds = User::where('role', 'user')->pluck('id')->toArray();
        $productIds = Product::pluck('id')->toArray();

        if (empty($userIds) || empty($productIds)) {
            return;
        }

        $comments = [
            ['Дуже задоволений покупкою, рекомендую.', 5],
            ['Швидка доставка, книга в ідеальному стані.', 5],
            ['Якість друку на висоті, варто своїх грошей.', 4],
            ['Цікавий артбук, багато неопублікованих матеріалів.', 5],
            ['Подарував другу — він в захваті.', 5],
            ['Не очікував такого рівня деталізації, супер.', 4],
            ['Обкладинка міцна, папір щільний.', 4],
            ['Колекція ілюстрацій дуже вражає.', 5],
            ['Добре упаковано, нічого не пошкодилось.', 5],
            ['Середній рівень, нічого особливого.', 3],
            ['Очікував трохи більше контенту за ціну.', 3],
            ['Деякі сторінки виглядають блідо.', 2],
            ['Не зовсім те, що очікував за описом.', 3],
            ['Довго чекав на доставку.', 2],
            ['Зміст непоганий, але палітурка слабкувата.', 3],
            ['За такі гроші очікував краще.', 2],
            ['Є кілька цікавих розворотів, решта середньо.', 3],
            ['Помилка в описі — інший формат прийшов.', 2],
            ['Кольори трохи відрізняються від очікуваних.', 3],
            ['Взагалі норм, але не вразило.', 3],
            ['Чудовий вибір для підборки референсів.', 5],
            ['Авторські коментарі додають цінності.', 4],
            ['Ідеально для натхнення та навчання.', 5],
            ['Вже купував інший артбук цього автора — цей теж топ.', 5],
            ['Розмір зручний, можна брати в дорогу.', 4],
            ['Мало тексту, переважно візуал.', 3],
            ['Частина ілюстрацій дрібна, важко розглядати.', 2],
            ['Ціна трохи кусається для такого обсягу.', 2],
            ['Загалом задоволений, буду замовляти ще.', 4],
        ];

        $created = 0;
        foreach ($comments as $index => $item) {
            $text = $item[0];
            $rating = $item[1];
            $productId = $productIds[$index % count($productIds)];
            $userId = $userIds[$index % count($userIds)];

            $exists = Review::where('product_id', $productId)->where('user_id', $userId)->where('comment', $text)->exists();
            if (!$exists) {
                Review::create([
                    'product_id' => $productId,
                    'user_id' => $userId,
                    'rating' => $rating,
                    'comment' => $text,
                ]);
                $created++;
            }
        }
    }
}
